package system.cli.api;

import java.io.IOException;

/**
 * Клиентское приложение (аналог psql),
 * которое подключается к серверу, общается по TCP и предоставляет простой REPL.
 *
 * Основные задачи:
 * - установить соединение с сервером (host, port);
 * - прочитать и вывести приветственное сообщение;
 * - в цикле:
 *     - читать команду пользователя из stdin;
 *     - отправлять её на сервер;
 *     - читать ответ до маркера "__END__" и выводить на экран;
 *     - при "exit"/"quit" — закрыть соединение и выйти.
 */
public interface Client {

    /**
     * Запускает клиентский REPL:
     * - подключается к серверу;
     * - общается с пользователем и сервером до команды "exit"/"quit".
     *
     * @throws IOException при ошибках соединения или ввода/вывода.
     */
    void start() throws IOException;
}